    {
        if(
            Auth::attempt(
                [
                    "email" => $request->get("email"), 
                    "password" => $request->get("password")
                ]
            )
        ){
            $request->session()->regenerate();
            return redirect("/empresa");
        }else{
            return redirect("/adm");
        }
    }

